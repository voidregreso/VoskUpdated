package org.vosk.demo;

public class Words {
    private Float conf;
    private Float end;
    private Float start;
    private String word;

    public Float getConf() {
        return conf;
    }

    public void setConf(Float conf) {
        this.conf = conf;
    }

    public Float getEnd() {
        return end;
    }

    public void setEnd(Float end) {
        this.end = end;
    }

    public Float getStart() {
        return start;
    }

    public void setStart(Float start) {
        this.start = start;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }
}
